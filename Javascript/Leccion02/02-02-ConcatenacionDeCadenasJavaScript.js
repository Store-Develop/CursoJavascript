//Concatenación de cadenas de texto en Javascript

var nombre = 'Juan';
var apellido = 'Pérez';

var nombreCompleto = nombre + " " +  apellido;
console.log(nombreCompleto);

//Otra forma
var nombreCompleto2 = "Carlos" + " " + "Lara";
console.log(nombreCompleto2);

//Contenación de cadena texto y numero
var x = nombre + 219;
console.log(x);

x = nombre + (2 + 4);
console.log(x);

x = 2 + 4 + nombre;
console.log(x);
