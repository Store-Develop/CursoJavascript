//Uso basico de var, let y const en Javascript.

/*Hoy en dia no es recomendado usar var para declarar variables.
En su lugar se utiliza (let) para declarar variables
Y (const) para declarar constantes.*/

//Declaración de variales

//De esta manera se pueden declara variables en javascript pero es una mala practica.
nombre = "Juan";
console.log(nombre);

//Buena practica dedeclaración de variables.
let nombrex = "Juan";
console.log(nombrex);


let apellido;
apellido = "Pérez";
console.log(apellido);
//Para declarar constantes utilizamos const.
const apellido2 = "Santana";