//El operador ternario en javascript es representado por el signo (?) y tiene casi la misma funcionalidad que el el (if else).

let resultado = (1 > 2) ? "verdadero" : "falso";
console.log(resultado);

//numero es par o impar con operador ternario.
let numero = 9;
resultado = (numero % 2 == 0) ? "Numero par" : "Numero Impar";
console.log(resultado);